#ifndef PROJECT_M_D_D_USEFUL_H
#define PROJECT_M_D_D_USEFUL_H
class X {
    int i;
public:
    X() {
        i = 0;
    }
    void set(int ii) {
        i = ii;
    }
    int read() const {
        return i;
    }
    int permute() {
        return i = i * 47;
    }
};

#endif //PROJECT_M_D_D_USEFUL_H
